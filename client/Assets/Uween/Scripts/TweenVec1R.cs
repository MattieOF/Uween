using UnityEngine;
using System.Collections;

public abstract class TweenVec1R : TweenVec1
{
    override public Vector3 vector
    {
        get
        {
            return GetTransform().localRotation.eulerAngles;
        }
        set
        {
            GetTransform().localRotation = Quaternion.Euler(value);
        }
    }
}